import { ErrorWrapper } from '../errors/ErrorWrapper';

/**
 * Class to represent an error generated by a try to run the app in a operational 
 * system different from win32, linux or darwin (macOs), if the app runs at all.
 * 
 * @since 0.0.1
 */
export class InvalidPlatformError extends ErrorWrapper {
  // Override
  protected _classifier = 'PLATFORM_ERROR';

  // Override
  protected _className = 'InvalidPlatformError';

  constructor(){
    super(`The operational system ${process.platform} is not supported.`);
  }
}